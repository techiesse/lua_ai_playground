
function colorFromInt(colorCode)
    r = bit.rshift(bit.band(colorCode, 0xff0000), 16)
    g = bit.rshift(bit.band(colorCode, 0x00ff00), 8)
    b = bit.band(colorCode, 0x0000ff)
    return {r, b, b}
end


